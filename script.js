$(function(){
	function fileToImage(file, cb) {
		var reader = new FileReader();
		reader.readAsDataURL(file);
		reader.onload = function() {
			var image = new Image();
			image.src = reader.result;
			image.onload = function() {
				cb(image);
			}
		}
	}
	$("#start").click(function(){
		// 画像を取得
		var imageFile = $("#selectFileInput").get(0).files[0];
		var canvas = $("#canvas").get(0);
		var context = canvas.getContext("2d");
		var canvas2 = $("#canvas2").get(0);
		var context2 = canvas2.getContext("2d");
		console.log(imageFile);
		fileToImage(imageFile, function(image) {
			console.log(image);
			canvas.width = image.width;
			canvas.height = image.height;
			canvas2.width = image.width;
			canvas2.height = image.height;
			context.drawImage(image, 0, 0);
			var imageData = context.getImageData(0, 0, image.width, image.height);
			var data = imageData.data;
			var dstData = context.createImageData(image.width, image.height);
			var dst = dstData.data;
			grayFilter(data, dst, image.width, image.height);
			context.putImageData(imageData, 0, 0);
			context2.putImageData(dstData, 0, 0);
		})
	})
})
